/*
    小技巧：命令行进入一个文件夹后，可以输入 code . 来使用code打开


    gitignore
        - 默认情况下，git会监视项目中的所有内容，即使未追踪。
        - 有一些内容我是不希望被git所监视所管理的，比如node_modules中的内容

        - 可以在项目中添加一个 .gitignore文件 ，来设置那些需要被git所忽略的文件
            - 在.gitignore文件中，一行就代表一个文件或文件夹，直接写要忽略的名字即可，
              或者写 *.js 这种写法，表示所有的js文件

*/